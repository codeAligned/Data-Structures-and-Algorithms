    public static void DFS(int node,int parent) {
        Stack<Integer> stack=new Stack<>();
        boolean push[]=new boolean[visit.length];
        stack.push(node);
        push[node]=true;
        while(!stack.isEmpty()) {
            int childnode = stack.pop(); // initially pops itself and then goes to connected nodes via iterator
            if (!visit[childnode]) {
                visit[childnode]=true;
                Iterator it = list[childnode].iterator();
                while (it.hasNext()) {
                    int nxtnode = (int) it.next();
                    {
                        if (!visit[nxtnode] && !push[nxtnode]) {
                            stack.push(nxtnode);
                            push[nxtnode] = true;
                            p1[parent] += 1;
                        }
                    }
                }
            }

        }
        return;
    }
